  FROM CCCOMCDT A, CCCOMCDT B
 WHERE A.GRPCODE LIKE 'SM%'
   AND A.GRPCODE NOT IN ('SM000','BMI','SM008','SM101','SM102','SM103','SM104','SM105','SM108','SM109','SM110','SM111'
                        ,'SM113','SM114','SM115','SM116','SM117','SM120','SM132','SM201','SM202','SM203','SM204','SM209'
