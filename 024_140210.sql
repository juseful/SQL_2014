SELECT A.PKGCODE, D.PKGNM, B.ORDCODE, C.ORDNAME, COUNT(A.PATNO) CNT
  FROM SMRSV00T A, MMEXMORT B, MMORDRCT C, SMPKGMST D
 WHERE A.ORDDATE BETWEEN TO_DATE('20130101','YYYYMMDD') AND TO_DATE('20131231','YYYYMMDD')
   AND A.CANCELTIME IS NULL
   AND A.PKGCODE = D.PKGCODE
   AND A.PATNO = B.PATNO
   AND A.ORDDATE = B.ORDDATE
   AND B.ORDCODE IN ('RC1180','RC1190','RC11901','RC11902','RC1200','RC1203','RC1240','RC2010','RC2020','RC2030'
                    ,'RC2050','RC2060','RC2070','RC2080','RC2110','RC3120','RC3226','RC3227','RC3251','RC3253','RC32542'
                    )
   AND B.PATFG = 'G'
   AND B.PROCSTAT > 'C'
   AND B.ORDCODE = C.ORDCODE
 GROUP BY A.PKGCODE, D.PKGNM, B.ORDCODE, C.ORDNAME
